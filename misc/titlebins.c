int save(const char *fileName, const unsigned char *data, size_t size)
{
    FILE *fp;

    fp = fopen(fileName, "wb");
    if(!fp)
        return -1;

    if (size != fwrite(data, 1, size, fp)) {
        fclose(fp);
        return -2;
    }

    fclose(fp);

    return 0;
}

