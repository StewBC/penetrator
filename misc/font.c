int data_size = sizeof(font_data) / sizeof (font_data[0]);

int main() {
    int i, j, k = 0;
    unsigned char a, b, c;
    for(i = 0; i < data_size; i+=2) {
        a = font_data[i];
        b = font_data[i+1];
        c =  (a & 0b10000000) | 
            ((a & 0b00100000) << 1) |
            ((a & 0b00001000) << 2) |
            ((a & 0b00000010) << 3) |
            ((b & 0b10000000) >> 4) | 
            ((b & 0b00100000) >> 3) |
            ((b & 0b00001000) >> 2) |
            ((b & 0b00000010) >> 1);
        printf(".byte $%02X\t;",c);
        for(j=7; j >= 0; j--) {
            if(c & (1<<j)) {
                printf("X");
            } else {
                printf("-");
            }
        }
        if(!k) {
            printf("\t%d", 32 + i / 16);
        }
        printf("\n");
        if(++k == 8) {
            k = 0;
            printf("\n");
        }
    }
    return 0;
}